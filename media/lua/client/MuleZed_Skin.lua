end




function MuleZed.isMuleZedDmg(fType)
   if not fType then return false end
   if luautils.stringStarts(fType, "Base.ZedDmg_MuleZed") then return true end
   return false
end

function MuleZed.clear(targ)

   if not targ then return end
    local vis = targ:getHumanVisual()
   if not vis then return end
    local bVis = vis:getBodyVisuals()
   if not bVis then return end

   for j = bVis:size() - 1, 0, -1 do
      local item = bVis:get(j)
      if item then
         local fType = item:getItemType()
         if fType and MuleZed.isMuleZedDmg(fType) then       
            vis:removeBodyVisualFromItemType(fType)
         end
      end
   end
	targ:resetModelNextFrame()
   
end





function MuleZed.CropArms(targ)
	targ = targ or getPlayer()
   local fTtpe = 'Base.MuleZed'

	local item = InventoryItemFactory.CreateItem(fTtpe)

	local inv = targ:getInventory()
   local equip = item 
   if inv then
      equip = inv:addItem(item);
   end
	targ:setWornItem(equip:getBodyLocation(), equip);
	triggerEvent("OnClothingUpdated", targ)
	targ:resetModelNextFrame()
end




