MuleZed = MuleZed or {}



function MuleZed.setRandWalk(zed)
	zed:setWalkType(tostring(ZombRand(1,6)));
end


function MuleZed.findID(int)
	local zombies = getCell():getObjectList()
	for i=zombies:size(),1,-1 do
		local zed = zombies:get(i-1)
		if instanceof(zed, "IsoZombie") then
			local zedId=tostring(zed:getOnlineID())
			if zedId and zedId == tostring(int) then return zed end
		end
	end
	return nil
end


function MuleZed.updateOpt()
    local sOpt = getSandboxOptions()
    sOpt:toLua()
    sOpt:sendToServer()
end



function MuleZed.getDist(zed, targ)
    if zed and targ then
        return math.floor(IsoUtils.DistanceTo(zed:getX(), zed:getY(), targ:getX(), targ:getY()))
    end
    return nil
end



function MuleZed.pause(seconds, callback)
    local start = getTimestampMs()
    local duration = seconds * 1000
    local tick

    tick = function()
        if getTimestampMs() - start >= duration then
            Events.OnTick.Remove(tick)
            if callback then callback() end
        end
    end

    Events.OnTick.Add(tick)
end

function MuleZed.checkDist(pl, sq)
	local dist = pl:DistTo(sq:getX(), sq:getY())
    return math.floor(dist)
end

function MuleZed.isWithinRange(pl, zed, range)
    range = range or 6
	local dist = pl:DistTo(zed:getX(), zed:getY())
    return dist <= range
end

function MuleZed.isClosestPl(pl, zed)
	if not pl or not zed then return false end    
	if not MuleZed.isMuleZed(zed) then return false end
	local plDist = MuleZed.checkDist(pl, zed)
	local compare = round(zed:distToNearestCamCharacter())
	if plDist == compare then
		return true
	end
	return false
end
